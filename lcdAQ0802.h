#ifndef lcdAQ0802 
#define lcdAQ0802

#include "Arduino.h"
#include <Wire.h>


//ここに#include文とプログラムがあります。



//  ------------------------------------------------------------
//  LCD(AQM0802)関係
//  ------------------------------------------------------------
#define LCDA 0x3e           // LCD address
#define CONTRAST 0x20         // for AQM0802@3.3V

//  ----------------------------------------
//  LCDにコマンド送信
//  ----------------------------------------
void lcdCmd(byte cmd);

//  ----------------------------------------
//  LCDにデータ送信
//  ----------------------------------------
void lcdData(byte dat) ;

void lcdPut(int x, int y, byte dat);

void lcdReg(int a, char *str);

//  ----------------------------------------
//  表示位置の指定
//  ----------------------------------------
void lcdLocate(byte col, byte row) ;

//  ----------------------------------------
//  初期化
//  ----------------------------------------
void lcdInit() ;
//  ----------------------------------------
//  全消去関数
//  ----------------------------------------
void lcdClear() ;

//  ----------------------------------------
//  文字列表示関数
//  ----------------------------------------
void lcdPrint( const char* cp ) ;

//  ----------------------------------------
//  LCDの1行目表示
//  ----------------------------------------
void lcdDisp1( const char* cp ) ;

//  ----------------------------------------
//  LCDの2行目表示
//  ----------------------------------------
void lcdDisp2( const char* cp ) ;
//  ----------------------------------------
//  8文字表示(普通は使わない)
//  ----------------------------------------
void lcdDisp8( const char* cp ) ;


#endif
